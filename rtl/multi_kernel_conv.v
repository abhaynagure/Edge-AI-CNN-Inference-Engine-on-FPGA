module multi_kernel_conv(

input [7:0] p1,p2,p3,
input [7:0] p4,p5,p6,
input [7:0] p7,p8,p9,

output [31:0] fm1,
output [31:0] fm2,
output [31:0] fm3,
output [31:0] fm4

);

//=========================
// Kernel 1 Wires
//=========================

wire [7:0] k11,k12,k13,k14,k15,k16,k17,k18,k19;

//=========================
// Kernel 2 Wires
//=========================

wire [7:0] k21,k22,k23,k24,k25,k26,k27,k28,k29;

//=========================
// Kernel 3 Wires
//=========================

wire [7:0] k31,k32,k33,k34,k35,k36,k37,k38,k39;

//=========================
// Kernel 4 Wires
//=========================

wire [7:0] k41,k42,k43,k44,k45,k46,k47,k48,k49;

//=========================
// Kernel Memories
//=========================

kernel_memory1 km1(
.w1(k11),.w2(k12),.w3(k13),
.w4(k14),.w5(k15),.w6(k16),
.w7(k17),.w8(k18),.w9(k19)
);

kernel_memory2 km2(
.w1(k21),.w2(k22),.w3(k23),
.w4(k24),.w5(k25),.w6(k26),
.w7(k27),.w8(k28),.w9(k29)
);

kernel_memory3 km3(
.w1(k31),.w2(k32),.w3(k33),
.w4(k34),.w5(k35),.w6(k36),
.w7(k37),.w8(k38),.w9(k39)
);

kernel_memory4 km4(
.w1(k41),.w2(k42),.w3(k43),
.w4(k44),.w5(k45),.w6(k46),
.w7(k47),.w8(k48),.w9(k49)
);

//=========================
// Conv Engine 1
//=========================

conv3x3 c1(

.p1(p1),.p2(p2),.p3(p3),
.p4(p4),.p5(p5),.p6(p6),
.p7(p7),.p8(p8),.p9(p9),

.w1(k11),.w2(k12),.w3(k13),
.w4(k14),.w5(k15),.w6(k16),
.w7(k17),.w8(k18),.w9(k19),

.y(fm1)

);

//=========================
// Conv Engine 2
//=========================

conv3x3 c2(

.p1(p1),.p2(p2),.p3(p3),
.p4(p4),.p5(p5),.p6(p6),
.p7(p7),.p8(p8),.p9(p9),

.w1(k21),.w2(k22),.w3(k23),
.w4(k24),.w5(k25),.w6(k26),
.w7(k27),.w8(k28),.w9(k29),

.y(fm2)

);

//=========================
// Conv Engine 3
//=========================

conv3x3 c3(

.p1(p1),.p2(p2),.p3(p3),
.p4(p4),.p5(p5),.p6(p6),
.p7(p7),.p8(p8),.p9(p9),

.w1(k31),.w2(k32),.w3(k33),
.w4(k34),.w5(k35),.w6(k36),
.w7(k37),.w8(k38),.w9(k39),

.y(fm3)

);

//=========================
// Conv Engine 4
//=========================

conv3x3 c4(

.p1(p1),.p2(p2),.p3(p3),
.p4(p4),.p5(p5),.p6(p6),
.p7(p7),.p8(p8),.p9(p9),

.w1(k41),.w2(k42),.w3(k43),
.w4(k44),.w5(k45),.w6(k46),
.w7(k47),.w8(k48),.w9(k49),

.y(fm4)

);

endmodule